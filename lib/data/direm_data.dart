  /// Tüm direnleri tek listede getir
  static List<String> getAllDiremler() {
    final allDiremler = <String>[];
    for (var category in categories) {
      allDiremler.addAll(category.diremler);
    }
    // Tekrarları kaldır
    return allDiremler.toSet().toList();
  }

  /// Kategoriye göre direnleri getir
  static List<String> getDiremlerByCategory(String categoryName) {
    final category = categories.firstWhere(
      (cat) => cat.name == categoryName,
      orElse: () => categories.first,
    );
    return category.diremler;
  }

  /// Direm kategorisini getir
  static DiremCategory? getCategoryByName(String categoryName) {
    try {
      return categories.firstWhere((cat) => cat.name == categoryName);
    } catch (e) {
      return null;
    }
  }
